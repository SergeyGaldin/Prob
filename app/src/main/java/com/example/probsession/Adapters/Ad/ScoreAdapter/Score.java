package com.example.probsession.Adapters.Ad.ScoreAdapter;

public class Score {
    String num, money;

    public Score() {
    }

    public Score(String num, String money) {
        this.num = num;
        this.money = money;
    }

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }

    public String getMoney() {
        return money;
    }

    public void setMoney(String money) {
        this.money = money;
    }
}
